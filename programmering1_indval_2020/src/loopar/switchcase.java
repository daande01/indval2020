package loopar;

public class switchcase {


	public static void main(String[] args) {

		int key =0;

		switch (key) {
		case 0:
			System.out.println("du har valt 0");
			break;
		case 1:
			System.out.println("du har valt 1");
			break;
		default:
			System.out.println("du har valt något annat än 0 eller 1");
			break;
		}




	}



}
